import math
x=int(input('Enter values for x and y :'))
y=int(input())
print('ceil(x)= ', math.ceil(x),'\ncopysign(x and y)=',math.copysign(x,y),'\nfactorial(x)= ',math.factorial(x),
    '\nfloor(X)= ',math.floor(x),
    '\nfmod(x and y)= ',math.fmod(x,y),'\n1power(x and y)= ',math.pow(x,y),'\nsquare root(x)= ',math.sqrt(x))
